
class condizioni_e_iteratori_o_cicli {
    public static void main(String[] args){

        //INIZIALIZZAZIONE VARIABILI
        boolean CONDIZIONE = false; 

        //OPERAZIONI
        if (CONDIZIONE == true){

            System.out.println("ciao a tutti");
            
        } // chiusura dell'if (caso VERO)
        else if (CONDIZIONE == false){

            System.out.println("arrivederci a tutti");

        } // chiusura else if (caso FALSO)
        else{

            System.out.println("non era presente");

        } // chiusura else (altro caso)

    } // chiusura del main

} // chiusura della classe

